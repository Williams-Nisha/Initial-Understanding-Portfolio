                <h1>User Interface</h1>

                <article><p>According to Wikipedia, <quote>"User interface design (UID) or user interface engineering is the design of user interfaces for machines and software, such as computers, home appliances, mobile devices, and other electronic devices, with the focus on maximizing the user experience."</quote></p>
                    <p>A good user interface design for mobile has:
                    <ul><li>Good Contrast</li>
                        <li>Clean Interface</li>
                        <li>Only as much design as needed to run app</li>
                        <li>Very few colors</li>
                        <li>Easy navigation</li>
                    </ul>
                    <h3>Examples of good user interface design:</h3>
                    <img src="images/design_example.jpg">
                    <p>This first example has excellent contrast with the colors. It uses three colors, teal, white and black. They do a good job of highlighting the focal points of the app through color and size.<p>

                        <img src="images/design_example1.jpg" height="723" width="250" >
                    <p>This example uses hot pink, white and black colors for its user interface. The icon buttons create visual hierarchy and it has a simple font style to give attention to the most important functions of the app.</p>
                    <p> Since this is an information form, there are hot pink dots on the side to help navigate you through the steps they would like you to accomplish. The most visual interest on the app is in the contact form.</p>

                    <img src="images/design_example2.jpg" height="604" width="300">
                    <div id="square"></div><p>This example has very little graphic design. It's design comes through typography. It uses pleasing sans-serif typography to give it a clean look. Everything is aligned together for a pleasing look and feel. The graphics they do use help to support the typography next to it. The contrast of orange helps to highlight the current choices pulled up in the app. </p>
                    <img src="images/design_example3.jpg"height="568" width="320" > 
                    <p>This final example is more of a graphical look. It uses the analogous colors of red and orange. I love how it has a bifold look to it that collapses to show the inner design. It has a very clean look and feel to the design. Notice how it only has as much information listed as is absolutely necessary.</p>
